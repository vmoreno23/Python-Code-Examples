user_miles = input("Enter miles driven: ")
miles = float(user_miles) #Converts the user input from string value to float value

user_gallons = input("Enter number of gallons used: ")
gallons = int(user_gallons) #Converts string value to integer value

MPG = miles / gallons #Equation for MPG 

print("MPG for the vehicle is:", MPG, "\nHave a nice day!")

